export function getSessionBySlug(slug: string): Session | undefined {
  return allSessions.find((s) => s.slug === slug);
}

export function getSessionsByBlock(blockNumber: BlockNumber): Session[] {
  return allSessions.filter((s) => s.block_number === blockNumber);
}

