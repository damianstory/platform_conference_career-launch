export function getSessionBySlug(slug: string): Session | undefined {
  return allSessions.find((s) => s.slug === slug);
}

export function getSessionsByBlock(blockNumber: BlockNumber): Session[] {
  return allSessions.filter((s) => s.block_number === blockNumber);
}

export function getSessionsByIndustry(industry: string): Session[] {
  return allSessions.filter(
    (s) => s.industry?.toLowerCase() === industry.toLowerCase()
  );
}

export function getAllIndustries(): string[] {
  const industries = allSessions
    .map((s) => s.industry)
    .filter((industry): industry is string => industry !== null);
  return Array.from(new Set(industries)).sort();
}
